#import "HDPicModle.h"

@implementation HDPicModle

+ (instancetype)initWithpicName:(NSString *)picName pic:(UIImage *)pic {
    HDPicModle *model = [[HDPicModle alloc] init];
    model.picName = picName;
    model.pic = pic;
    
    return model;
}

- (NSString *)description
{
    return [NSString stringWithFormat:@"<%@ : %p> \n{picName : %@ \n pic : %@ \n}", [self class], self,self.picName, self.pic];
}

@end
